    @RequiresApi(Build.VERSION_CODES.O)
    private fun generateDummyResponse(page: Int): RecordedApiResponse {
        val programs = mutableListOf<RecordedProgram>()
        val startIndex = (page - 1) * pageSize
        val endIndex = minOf(startIndex + pageSize, totalRecords)

        val now = ZonedDateTime.now()
        val formatter = DateTimeFormatter.ISO_OFFSET_DATE_TIME

        for (i in startIndex until endIndex) {
            val startTime = now.minusHours((i * 2).toLong())
            val endTime = startTime.plusHours(1)

            // 放送局の割当（バラけるようにインデックス調整）
            val selectedChannel = channelMaster[(i * 3) % channelMaster.size]

            // シリーズとジャンルの取得
            val seriesData = masterSeriesList[i % masterSeriesList.size]
            val episodeNum = (i / masterSeriesList.size) + 1

            // ランダムノイズ付与
            val titleSuffix = when {
                Random.nextFloat() > 0.9f -> " 拡大SP"
                Random.nextFloat() > 0.8f -> " [字] 傑作選"
                else -> " 第${episodeNum}話"
            }
            val programTitle = "${seriesData.title}$titleSuffix"

            // アニメ・ドラマは基本的にエピソードありとする
            val isEpisodicFlag =
                seriesData.majorGenre == "アニメ・特撮" || seriesData.majorGenre == "ドラマ"

            val massiveText = "あいうえおかきくけこ".repeat(1000)

            val program = RecordedProgram(
                id = 10000 + i,
                title = programTitle,
                description = "【負荷テスト用データ】 $programTitle の詳細テキストです。大量の文字列が入ることを想定しています。 $massiveText",
                detail = mapOf(
                    "番組内容" to "ダミー詳細テキストです。大量の文字列が入ることを想定しています。$massiveText",
                    "番組内容2" to "ダミー詳細テキストです。大量の文字列が入ることを想定しています。$massiveText",
                    "番組内容3" to "ダミー詳細テキストです。大量の文字列が入ることを想定しています。$massiveText"
                ),
                startTime = startTime.format(formatter),
                endTime = endTime.format(formatter),
                duration = 3600.0,
                isPartiallyRecorded = false,
                isEpisodic = isEpisodicFlag,
                channel = RecordedChannel(
                    id = selectedChannel.first,
                    displayChannelId = "",
                    type = selectedChannel.second,
                    name = selectedChannel.third,
                    channelNumber = ""
                ),
                recordedVideo = RecordedVideo(
                    id = 10000 + i,
                    status = "Recorded",
                    filePath = "/api/streams/video/${10000 + i}/1080p/playlist.m3u8",
                    recordingStartTime = startTime.format(formatter),
                    recordingEndTime = endTime.format(formatter),
                    duration = 3600.0,
                    containerFormat = "mpegts",
                    videoCodec = "mpeg2video",
                    audioCodec = "aac",
                    hasKeyFrames = true,
                    thumbnailInfo = null
                ),
                // ★ 動的にジャンルを紐づけ
                genres = listOf(EpgGenre(seriesData.majorGenre, seriesData.minorGenre)),
                isRecording = false,
                playbackPosition = 0.0
            )
            programs.add(program)
        }

        return RecordedApiResponse(
            total = totalRecords,
            recordedPrograms = programs
        )
    }
